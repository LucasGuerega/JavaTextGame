package model;

public class player {
		private int HP;
		private int ATK;
		private String name;
		public int getHP() {
			return HP;
		}
		public void setHP(int hP) {
			HP = hP;
		}
		public int getATK() {
			return ATK;
		}
		public void setATK(int aTK) {
			ATK = aTK;
		}
		public String getName() {
			return name;
		}
		public void setName(String name) {
			this.name = name;
		}

}
